package Day6;

public class forLoop {
    public static void main(String[] args) {
        // Print numbers from 1 to 5
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        //print even Numbers
        for (int i = 2; i <= 10; i += 2) {
            System.out.println(i);
        }
        //print reverse counting
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }

    }
}
